int checkutf8unicodestr(const unsigned char *s)
{
	while(*s)
	{
		unsigned int val;

		if(!(*s&0x80))
		{
			s++;
			continue;
		}
		else if((*s&0xe0)==0xc0)
		{
			val=((unsigned int)(*s++&0x1f))<<6;
			val|=(unsigned int)(*s++&0x3f);
		}
		else if((*s&0xf0)==0xe0)
		{
			val=((unsigned int)(*s++&0x0f))<<12;
			val|=((unsigned int)(*s++&0x3f))<<6;
			val|=(unsigned int)(*s++&0x3f);
		}
		else if((*s&0xf8)==0xf0)
		{
			val=((unsigned int)(*s++&0x07))<<18;
			val|=((unsigned int)(*s++&0x3f))<<12;
			val|=((unsigned int)(*s++&0x3f))<<6;
			val|=(unsigned int)(*s++&0x3f);
		}
		else if((*s&0xfc)==0xf8)
		{
			val=((unsigned int)(*s++&0x03))<<24;
			val|=((unsigned int)(*s++&0x3f))<<18;
			val|=((unsigned int)(*s++&0x3f))<<12;
			val|=((unsigned int)(*s++&0x3f))<<6;
			val|=(unsigned int)(*s++&0x3f);
		}
		else if((*s&0xfe)==0xfc)
		{
			val=((unsigned int)(*s++&0x01))<<30;
			val|=((unsigned int)(*s++&0x3f))<<24;
			val|=((unsigned int)(*s++&0x3f))<<18;
			val|=((unsigned int)(*s++&0x3f))<<12;
			val|=((unsigned int)(*s++&0x3f))<<6;
			val|=(unsigned int)(*s++&0x3f);
		}
		else return -1;
		if(val<0x10000)
		{
			if(map00[val>>5]&(1<<(val&0x1f)))continue;
			else return -1;
		}
		if(val<0x10880)
		{
			val-=0x10000;
			if(map10[val>>5]&(1<<(val&0x1f)))continue;
			else return -1;
		}
		if(val<0x1d000)return -1;
		if(val<0x1d800)
		{
			val-=0x1d000;
			if(map1d[val>>5]&(1<<(val&0x1f)))continue;
			return -1;
		}
		if(val<0x20000)return -1;
		if(val<=0x2a6d6)continue;
		if(val<0x2f800)return -1;
		if(val<=0x2fa1d)continue;
		if(val==0xe0001)continue;
		if(val<0xe0020)return -1;
		if(val<=0xe007f)continue;
		if(val<0xe0100)return -1;
		if(val<=0xe01ef)continue;
		if(val<0xf0000)return -1;
		if(val<=0xffffd)continue;
		if(val<0x100000)return -1;
		if(val<=0x10fffd)continue;
		return -1;
	}
	return 0;
}
