unsigned int ucs4_normalize(unsigned int val)
{
	if(val<0x600)return map00_06[val];
	else if(val>=0xd800&&val<=0xf8ff)return 0x20;
	else switch(val>>8)
	{
	case 0x06:
		if(val==0x06dd)return 0x20;
		break;
	case 0x07:
		if(val==0x070f)return 0x20;
		break;
	case 0x16:
		if(val==0x1680)return 0x20;
		break;
	case 0x18:
		if(val==0x180e)return 0x20;
		break;
	case 0x1e:
	case 0x1f:
	case 0x20:
	case 0x21:
		return map1e_21[val-0x1e00];
	case 0x24:
		return map24_24[val-0x2400];
	case 0x2f:
		if(val>=0x2ff0&&val<=0x2ffb)return 0x20;
		break;
	case 0x30:
		if(val==0x3000)return 0x20;
		break;
	case 0x33:
		return map33_33[val-0x3300];
	case 0xfb:
		switch(val)
		{
		case 0xfb00:
		case 0xfb01:
		case 0xfb02:
		case 0xfb03:
		case 0xfb04:
			return 0x0066;
		case 0xfb05:
		case 0xfb06:
			return 0x0073;
		case 0xfb13:
		case 0xfb14:
		case 0xfb15:
		case 0xfb17:
			return 0x0574;
		case 0xfb16:
			return 0x057e;
		}
		break;
	case 0xfd:
		if(val>=0xfdd0&&val<=0xfdef)return 0x20;
		break;
	case 0xfe:
		if(val==0xfeff)return 0x20;
		break;
	case 0xff:
		return mapff_ff[val-0xff00];
	case 0x104:
		if(val>=0x10400&&val<=0x10425)return val+0x28;
		break;
	case 0x1d1:
		if(val>=0x1d173&&val<=0x1d17a)return 0x20;
		break;
	case 0x1d4:
	case 0x1d5:
	case 0x1d6:
	case 0x1d7:
		return map1d4_1d7[val-0x1d400];
	case 0x1ff:
	case 0x2ff:
	case 0x3ff:
	case 0x4ff:
	case 0x5ff:
	case 0x6ff:
	case 0x7ff:
	case 0x8ff:
	case 0x9ff:
	case 0xaff:
	case 0xbff:
	case 0xcff:
	case 0xdff:
		if((val&0xfffe)==0xfffe)return 0x20;
		break;
	case 0xe00:
		if(val<0xe0080)return mape00_e00[val-0xe0000];
		break;
	}
	return val;
}
